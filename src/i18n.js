export function t(key, lang = 'en') {
  const L = SUPPORTED_LANGS.includes(lang) ? lang : 'en'
  return STRINGS[L]?.[key] ?? STRINGS.en[key] ?? key
}

// Simple formatter: replaces {name} placeholders with args[name].
// Use when you have interpolation: tf('shop.lotUnlockHint', lang, { n: 5, icon: '🏪', label: 'Shop' })
export function tf(key, lang, args = {}) {
  let str = t(key, lang)
  for (const [k, v] of Object.entries(args)) {
    str = str.split('{' + k + '}').join(v)
  }
  return str
}
